var numOne = 1;
var numTwo = 2;
// Addition
console.log("Addition:");
console.log(numOne + numTwo);
// Subtraction
console.log("Subtraction:");
console.log(numOne - numTwo);
// Multiplication
numOne = 10;
numTwo = 20;
console.log("Multiplication:");
console.log(numOne * numTwo);
// Division
console.log("Division:");
console.log(numTwo / numOne);
// Modulus
console.log("Modulus:");
console.log(numTwo % numOne);
// Incrementor
console.log("Incrementor:");
console.log(++numOne);
var x = 0;
while (x < 10) {
    console.log(x);
    x++;
}
// Decrementor
console.log("Incrementor:");
var x = 10;
while (x > 0) {
    console.log(x);
    x--;
}
